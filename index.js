function add(a, b) {
  return (a + b);
}

function subtract(a, b) {
  return (a - b);
}

function multiply(a, b) {
  return (a * b);
}

function divide(a, b) {
  return (a / b);
}

function increment(n) {
  return (++n);
}

function decrement(n) {
  return (--n);
}

function makeInt(n) {
  let newN = parseInt(n, 10);
  return (newN);
}

function preserveDecimal(n) {
  let anotherN = parseFloat(n);
  return(anotherN);
}